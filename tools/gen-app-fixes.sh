#!/bin/bash
# 應用示例錯字格單獨重生
set -u
cd "$(dirname "$0")"
GEN=~/.claude/skills/codex-imagegen/codex-imagegen.sh
COMMON="橫式構圖的迷你設計成品,主標題文字大而清晰,可有少量小型輔助文字與裝飾。所有主標題中文字必須是筆畫正確的繁體中文字形,不能出現錯字、多字、漏字或簡體字。"
gen(){ bash "$GEN" "$COMMON $1" "$2" </dev/null || echo "FAIL $2"; }
