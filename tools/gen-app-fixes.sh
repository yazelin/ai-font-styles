gen "主標題:「強觀點誇張字」(共五個字,一字不多一字不少),誇張變形強調字、字級大小錯落,白字與紅字搭配。場景:犀利評論短影音封面,黑底裂紋放射線加驚嘆號。特別注意:「觀」只能出現一次「見」部件,整行不得多出任何多餘的字。" appfix-048.png
gen "主標題:「虛擬空間字」(共五個字,一字不多一字不少),數位線框立體字。場景:虛擬建築作品集封面,藍色透視網格城市。特別注意:「虛」必須是繁體字形,下半部是兩豎立在一橫上(丘形),絕對不能寫成簡體「虚」的「业」形。" appfix-063.png
gen "主標題:「機甲裝甲字」(共五個字,一字不多一字不少),裝甲切面銳利金屬字。場景:機甲科幻遊戲封面,黑色機器人剪影火花。特別注意:「裝」必須是繁體字形,左上部件是「爿」,絕對不能寫成簡體「装」的「丬」。" appfix-066.png
gen "主標題:「禪意體」(共三個字,一字不多一字不少),空靈淡墨毛筆字。場景:禪意茶品牌視覺,枯山水石苔竹影大量留白。特別注意:「禪」必須是繁體字形,右邊是「單」(上方有兩個口),絕對不能寫成簡體或日文的「禅」。" appfix-092.png
